function mouseover()
{
let element2 = document.querySelector("div").children[0];
element2.style.background = "orange" 
element2.style.color = "black"

element2.innerHTML = "hello zoomers"
}

function mouseout()
{
let element2  = document.querySelector("div").children[0];
element2.style.background = "skyblue" 
element2.style.color = "red"

element2.innerHTML = "cdacian is on fire"



}
